Предложи точна ценова точка (числово, в лв.) за поне 3 услуги, обоснована с конкретни данни от таблицата. Формат: "Препоръчвам [X лв.] за [услуга], защото медианата на пазара е [Y лв.] (вж. [Конкурент] = [цена], [Конкурент] = [цена])"

Таблица с конкурентни цени:
{{Екстрактор на данни}}
PROMPT,
        ]);

        // ── Agent 23: Финален доклад (бивш Създател на съдържание) ──────
        // Problem: todorov/bggpt generates marketing copy, ignores structured instructions.
        // Fix: switch to gemma2:9b + explicit dual-section scaffold with BLANK FIELDS
        //      that the model must fill in (fill-in-the-blank is more reliable than rules).
        Agent::find(23)?->update([
            'model' => 'gemma2:9b',
            'type'  => 'analyzer',
            'role'  => 'Ти генерираш финален двусекционен доклад. СЕКЦИЯ 1 е конкурентен ценови анализ с конкретни числа. СЕКЦИЯ 2 е маркетинг текст, базиран на данните от Секция 1. ЗАБРАНЕНО е да измисляш URL адреси, телефонни номера или цени, които не са в предоставените данни.',
            'prompt_template' => <<<'PROMPT'
Изгответи финален доклад за {{company_description}} в точно ДВЕ СЕКЦИИ по-долу.
