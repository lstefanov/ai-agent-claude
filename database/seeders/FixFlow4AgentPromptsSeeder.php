 *  #4 Agent 24 (QA): doesn't check for concrete prices
 */
class FixFlow4AgentPromptsSeeder extends Seeder
{
    public function run(): void
    {
        // ── Agent 25: Екстрактор на данни ────────────────────────────────
        // Problem: output 2-column table instead of 5-column; competitor names
        //          were embedded in descriptions instead of own column.
        // Fix: add filled example row so the model knows exactly what each column means.
        Agent::find(25)?->update([
            'role' => 'Ти си специалист по структуриране на данни. Извличаш САМО конкретни ценови факти от текст и ги представяш в точно зададен таблечен формат. ЗАБРАНЕНО е да добавяш коментари, обяснения, обобщения или допълнителен текст — САМО таблицата.',
