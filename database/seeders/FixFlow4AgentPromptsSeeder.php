PROMPT,
            'output_format' => null,
            'config' => ['temperature' => 0.3],
        ]);

        // ── Agent 24: Контролер на качество ──────────────────────────────
        // Problem: QA gave 65/100 but didn't catch invented URLs/phones.
        //          Didn't verify presence of prices in BOTH sections.
        // Fix: explicit scoring criteria tied to concrete data requirements.
        Agent::find(24)?->update([
            'role' => 'Ти си QA верификатор за competitive intelligence доклади. Оценяваш САМО конкретността на данните, не стила или граматиката.',
            'prompt_template' => <<<'PROMPT'
Провери финалния доклад за следните ЗАДЪЛЖИТЕЛНИ критерии:

