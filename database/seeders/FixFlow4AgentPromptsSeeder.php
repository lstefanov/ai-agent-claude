- Колона "Цена": точното число с валута (лв. или €)
- Колона "Тип": фитнес абонамент / еднократно / групови / пилатес / йога / спа / басейн
- Колона "Линк": само домейна (без https://)
- Ако линк липсва: пиши "n/a"
- НЕ пропускай ред само защото линкът липсва
- Всяка цена = отделен ред (ако конкурент има 3 тарифи → 3 реда)

