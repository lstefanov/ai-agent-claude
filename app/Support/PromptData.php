<?php

namespace App\Support;

/**
 * Подготвя структурирани данни за вграждане в LLM промпт така, че моделът никога да не
 * вижда — и съответно да не повтаря — технически стойности като null. Липсващите данни
 * стават човешки на български, за да не изтичат термини като „null/undefined" в текста,
 * показан на потребителя.
 */
class PromptData
{
    /** Предпазител за системните промптове, които генерират текст за потребителя. */
    public const NO_TECH_TERMS = 'Никога не използвай технически думи като „null", „undefined", '
        .'„NaN" или „None" — за липсваща информация пиши „няма", „не е намерено" или „няма данни".';

    /**
     * Рекурсивно заменя null с човешки етикет (по подразбиране „няма данни"), за да не
     * достига литералът null до промпта. Ключовете остават непокътнати — сменя се само
     * стойността. Връща структурата готова за json_encode.
     *
     * @param  mixed  $value
     * @return mixed
     */
    public static function humanize($value, string $missing = 'няма данни')
    {
        if ($value === null) {
            return $missing;
        }

        if (is_array($value)) {
            return array_map(fn ($v) => self::humanize($v, $missing), $value);
        }

        return $value;
    }
}
